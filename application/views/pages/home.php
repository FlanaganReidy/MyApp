<h2>So you want to play Lancer?</h2>
<div class = "body-div">
<img class = "main-image" src="assets/images/Lancer-cover.jpg" alt="Lancer cover artwork">
<p>
Lancer is a tabletop role playing game centered on shared narratives, customizable mechs, and the pilots who crew them.
<br>
<br>
Set many thousands of years from the present day, Lancer imagines a future where a survivor humanity has spread to the stars after weathering terrible ecological collapse on Earth -- the end of the Anthropocene as a consequence of unrestrained consumption and poor stewardship.
<br>
<br>
With a survival imperative motivated by this cultural history, humanity’s organizing body, Union, seeks to manage the galaxy to ensure we’ll never again be faced with extinction. They are not perfect in this endeavor; the narrative present of Lancer imagines a future where Union’s central body has been gripped by popular revolution, and the new governing body seeks to rectify the crimes of the old.
<br>
<br>
Lancer features a mix of gritty, mud-and-lasers military science fiction and mythic science fantasy. In the setting, conscript pilots mix ranks with flying aces, mercenary guns-for-hire brawl with secretive corporate agents, and relativistic paladins cross thermal lances with causality-breaking, unknowable beings.
<br>
<br>
Players of Lancer adopt the roles of mech pilots -- comrades together in a galaxy of danger and hope. Some groups will fight for Union, working to rectify the crimes of previous administrations. Others, for a corpro-state, working to advance private interests while lining their pockets. Yet others will fight for groups acting in opposition to those in power, or for themselves.
</p>
